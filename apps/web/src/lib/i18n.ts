export type AppLanguage = 'uz' | 'en'

export const LANGUAGE_STORAGE_KEY = 'teacher-assistant-language'
export const DEFAULT_LANGUAGE: AppLanguage = 'uz'

let currentLanguage: AppLanguage = DEFAULT_LANGUAGE

export function isAppLanguage(value: string | null | undefined): value is AppLanguage {
  return value === 'uz' || value === 'en'
}

export function getStoredLanguage() {
  if (typeof window === 'undefined') {
    return DEFAULT_LANGUAGE
  }

  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY)
  return isAppLanguage(stored) ? stored : DEFAULT_LANGUAGE
}

export function getCurrentLanguage() {
  return currentLanguage
}

export function setCurrentLanguage(language: AppLanguage) {
  currentLanguage = language

  if (typeof window !== 'undefined') {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language)
    document.documentElement.lang = language
  }
}

